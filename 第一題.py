    for j in range (10) :       # 每一組的第一行
        print("*+", end = '')   # 輸出第一行裡的字 '*+' 為一個unit
    print()                     # 換行
    print()                     # 換行
    for k in range (10) :       # 每一組的第二行
        print("+*", end = '')   # 輸出第二行裡的字 '+*' 為一個unit
    print()                     # 換行
    print()                     # 換行
    

